llr(3)=scaling*(dglobal([-17,-19,-21,-23,-25,-27,-29,-31,17,19,21,23,25,27,29,31],I,P) - dglobal([-1,-3,-5,-7,-9,-11,-13,-15,1,3,5,7,9,11,13,15],I,P)) ;

llr(4)=scaling*(dglobal([-17,-19,-21,-23,-25,-27,-29,-31,17,19,21,23,25,27,29,31],Q,P) - dglobal([-1,-3,-5,-7,-9,-11,-13,-15,1,3,5,7,9,11,13,15],Q,P)) ;

llr(5)=scaling*(dglobal([-31,-29,-27,-25,-1,-3,-5,-7,1,3,5,7,25,27,29,31],I,P) - dglobal([-23,-21,-19,-17,-15,-13,-11,-9,9,11,13,15,17,19,21,23],I,P)) ;

llr(6)=scaling*(dglobal([-31,-29,-27,-25,-1,-3,-5,-7,1,3,5,7,25,27,29,31],Q,P) - dglobal([-23,-21,-19,-17,-15,-13,-11,-9,9,11,13,15,17,19,21,23],Q,P)) ;


