llr(9)=scaling*(dglobal([-31,-25,-23,-17,-15,-9,-7,-1,1,7,9,15,17,23,25,31],I,P) - dglobal([-29,-27,-21,-19,-13,-11,-5,-3,3,5,11,13,19,21,27,29],I,P)) ;

llr(10)=scaling*(dglobal([-31,-25,-23,-17,-15,-9,-7,-1,1,7,9,15,17,23,25,31],Q,P) - dglobal([-29,-27,-21,-19,-13,-11,-5,-3,3,5,11,13,19,21,27,29],Q,P)) ;

end



    

